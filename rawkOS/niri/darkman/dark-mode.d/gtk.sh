#!/usr/bin/env bash

# Set GTK theme to dark variant
gsettings set org.gnome.desktop.interface gtk-theme 'Catppuccin-Mocha-Standard-Teal-Dark'

# Set icon theme to dark variant
gsettings set org.gnome.desktop.interface icon-theme 'Papirus-Dark'

# Update cursor theme if needed
gsettings set org.gnome.desktop.interface cursor-theme 'Catppuccin-Mocha-Dark'